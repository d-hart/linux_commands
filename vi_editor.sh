# Command Mode
h - Left
j - Down
k - Up
l - Right

x - Delete
dd - Delete whole line

yy - Copy a line
p - Paste

CTRL+u - Scroll Up
CRTL+d - Scroll Down

: - Command
:w - Save
:q - Quit(Discard)
:wq - Save & Quit

/of - Find(of)
    n - jump to the next occurrence